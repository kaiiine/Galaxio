    private void cbOrdiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbOrdiActionPerformed
        // ComboBox permettant de définir le niveau de l'ordinateur
        String niveau = (String) cbOrdi.getSelectedItem();
        if(niveau.equals("Facile")){
            nivOrdi="Facile";
        }else if(niveau.equals("Moyen")){
            nivOrdi="Moyen";
        }else if(niveau.equals("Difficile")){
            nivOrdi="Difficile";
        }else{
            nivOrdi=null;
            cbPlateau.setSelectedItem("Choisir un Plateau");
        }
        //Affichage des composants ou non selon les valeurs des TextFields et/ou du niveau de l'ordinateur
        if((deuxJoueurs==false && nom1.length()>0 && nivOrdi!=null)){
            lPlateau.setVisible(true);
            cbPlateau.setVisible(true);
        }else{
            lPlateau.setVisible(false);
            cbPlateau.setVisible(false);
            cbPlateau.setSelectedItem("Choisir un Plateau");
        }
    }//GEN-LAST:event_cbOrdiActionPerformed

