    private void cbModeJeuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbModeJeuActionPerformed
        // ComboBox afin de connaître le mode jeu souhaité. 
        String jeu = (String) cbModeJeu.getSelectedItem();
        Toolkit t = Toolkit.getDefaultToolkit();
        Image img; // t.getImage(DOSS_IMAGES+"basc_OUI.png");
        
        if(jeu.equals("1 vs 1")){   //Affichage des composants souhaités selon le mode de jeu
            modeJeu=0;
            img=t.getImage(DOSS_IMAGES+"Premier_Astronaute.png");   //Changement du nom du label pour le joueur selon le mode de jeu
            lNom1.setSize(324,81);
            ((FAccueil) this.getParent()).afficher(img,lNom1);
            tfNom1.setText(null);
            tfNom2.setText(null);
            lNom1.setVisible(true);
            tfNom1.setVisible(true);
            lNom2.setVisible(true);
            tfNom2.setVisible(true);
            deuxJoueurs=true;
            lOrdi.setVisible(false);
            cbOrdi.setVisible(false);
            cbPlateau.setSelectedItem("Choisir un Plateau");
            cbOrdi.setSelectedItem("Niveau Ordi");
        }else if(jeu.equals("1 vs Ordi")){  //Affichage des composants souhaités selon le mode de jeu
            modeJeu=1;
            img=t.getImage(DOSS_IMAGES+"Nom.png");  //Changement du nom du label pour le joueur selon le mode de jeu
            lNom1.setSize(350,50);
            ((FAccueil) this.getParent()).afficher(img,lNom1);
            tfNom1.setText(null);
            tfNom2.setText(null);
            nom2="";
            lNom1.setVisible(true);
            tfNom1.setVisible(true);
            lOrdi.setVisible(true);
            cbOrdi.setVisible(true);
            lNom2.setVisible(false);
            tfNom2.setVisible(false);
            deuxJoueurs=false;
        }else{  //On enlève les composants graphique si aucun mode de jeu n'a été sélectionné
            lNom1.setVisible(false);
            tfNom1.setVisible(false);
            lNom2.setVisible(false);
            tfNom2.setVisible(false);
            lPlateau.setVisible(false);
            cbPlateau.setVisible(false);
            lOrdi.setVisible(false);
            cbOrdi.setVisible(false);
            cbPlateau.setSelectedItem("Choisir un Plateau");
            cbOrdi.setSelectedItem("Niveau Ordi");
        }
    }//GEN-LAST:event_cbModeJeuActionPerformed

    private void cbPlateauActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbPlateauActionPerformed
        // ComboBox permettant de choisir le type de plateau souhaité
        Toolkit t = Toolkit.getDefaultToolkit();
        String cases = (String) cbPlateau.getSelectedItem();
        bTerminer.setSize(208,77);
        if(cases.equals("3x3")){    //Modification 
            nbrCases=3;
            if(nouveau){    //Si une nouvelle partie est créée on change l'image du bouton par "créer", sinon par "charger"
                Image img10 = t.getImage(DOSS_IMAGES+"Creer.png");
                bTerminer.setSize(208,77);
                ((FAccueil) this.getParent()).afficher(img10,bTerminer);
            }else{
                Image img10 = t.getImage(DOSS_IMAGES+"Charger.png");
                bTerminer.setSize(208,77);
                ((FAccueil) this.getParent()).afficher(img10,bTerminer);
            }
        }else if(cases.equals("4x4")){
            nbrCases=4;
            if(nouveau){
                Image img10 = t.getImage(DOSS_IMAGES+"Creer.png");
                ((FAccueil) this.getParent()).afficher(img10,bTerminer);
            }else{
                Image img10 = t.getImage(DOSS_IMAGES+"Charger.png");
                ((FAccueil) this.getParent()).afficher(img10,bTerminer);
            }
        }else if(cases.equals("5x5")){
            nbrCases=5;
            if(nouveau){
                Image img10 = t.getImage(DOSS_IMAGES+"Creer.png");
                ((FAccueil) this.getParent()).afficher(img10,bTerminer);
            }else{
                Image img10 = t.getImage(DOSS_IMAGES+"Charger.png");
                ((FAccueil) this.getParent()).afficher(img10,bTerminer);
            }
        }else if(!nouveau){ //Affichage ou non du bouton bTerminer selon si on charge une partie ou non
            bTerminer.setVisible(true);
        }else{
            bTerminer.setVisible(false);
        }
    }//GEN-LAST:event_cbPlateauActionPerformed

