    private void updateTextField(String nom) {
        // Récupérer le texte des TextFields en temps réél
        
        if(nom.equals("nom1")){
            
            nom1 = tfNom1.getText();
        }else{
            nom2 = tfNom2.getText();
        }
        //Affichage des composants souhaités ou non si les TextFields ne sont pas vides et/ou si le niveau de l'ordinateur n'est pas nul
        if((deuxJoueurs==false && nom1.length()>0 && nivOrdi!=null) || (deuxJoueurs==true && nom1.length()>0 && nom2.length()>0)){
            lPlateau.setVisible(true);
            cbPlateau.setVisible(true);
        }else{
            lPlateau.setVisible(false);
            cbPlateau.setVisible(false);
            bTerminer.setVisible(false);
            cbPlateau.setSelectedItem("Choisir un Plateau");
        }
        if(!nouveau){
            bTerminer.setVisible(true);
        }
        
    }
    
    
    
